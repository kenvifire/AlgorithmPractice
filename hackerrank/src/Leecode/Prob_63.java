    public int uniquePathsWithObstacles(int[][] obstacleGrid) {
        long[][] dp = new long[obstacleGrid.length][obstacleGrid[0].length];

        for (int i = 0; i < obstacleGrid.length; i++) {
            for (int j = 0; j < obstacleGrid[0].length; j++) {
                if(obstacleGrid[i][j] == 1) {
                    dp[i][j] = 0;
                } else if (i - 1 >= 0 && j - 1 >= 0) {
                    dp[i][j]  = (obstacleGrid[i-1][j] == 1 ? 0 : 1) + (obstacleGrid[i][j-1] == 1 ? 0 : 1);
                } else {
                    dp[i][j] = 1;
                }
            }
        }

        for (int i = 0; i < dp.length; i++) {
            for (int j = 0; j < dp[0].length; j++) {
               if(i == 0 && j == 0)  continue;
               dp[i][j] = obstacleGrid[i][j] == 1 ? 0 : (i - 1 >= 0 ? dp[i-1][j] : 0) + (j - 1 >= 0 ? dp[i][j-1] : 0);
            }
        }
        return (int)dp[obstacleGrid.length - 1] [obstacleGrid[0].length - 1];


    }


